class CircularQueue
  attr_reader :buffer

  def initialize(buffer_size)
    @buffer = []
    @max_size = buffer_size
  end

  def enqueue(num)
    @buffer.shift if @buffer.length == @max_size
    @buffer.push(num)
  end

  def dequeue
    @buffer.shift
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
